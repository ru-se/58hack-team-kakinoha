// チャンク分割設定（句読点で区切る）
const CHUNK_DELIMITERS = /([。、！？\n]+)/;

/**
 * デモテキストを句読点・改行で区切ったチャンク配列に変換する。
 * コア機能には依存しない純粋な文字列処理。
 */
export const splitDemoIntoChunks = (text: string): string[] => {
    const parts = text.split(CHUNK_DELIMITERS);
    const chunks: string[] = [];
    for (let i = 0; i < parts.length; i += 2) {
        const word = parts[i] ?? '';
        const delimiter = parts[i + 1] ?? '';
        const chunk = word + delimiter;
        if (chunk.trim()) chunks.push(chunk);
    }
    return chunks;
};
